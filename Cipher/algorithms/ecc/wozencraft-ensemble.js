  /**
 * WozencraftEnsemble cipher instance implementing Feed/Result pattern
 * @class
 * @extends {IBlockCipherInstance}
 */

  class WozencraftEnsembleInstance extends IErrorCorrectionInstance {
    /**
   * Initialize Algorithm cipher instance
   * @param {Object} algorithm - Parent algorithm instance
   * @param {boolean} [isInverse=false] - Decryption mode flag
   */

    constructor(algorithm, isInverse = false) {
      super(algorithm);
      this.isInverse = isInverse;
      this.result = null;

      // Specific generator matrix for Wozencraft [4,2] code
      // In practice, this would be randomly generated
      this.generator = [
        [1, 0, 1, 1],  // First basis vector
        [0, 1, 1, 0]   // Second basis vector
      ];
    }

    /**
   * Feed data to cipher for processing
   * @param {uint8[]} data - Input data bytes
   * @throws {Error} If key not set
   */

    Feed(data) {
      if (!Array.isArray(data)) {
        throw new Error('WozencraftEnsembleInstance.Feed: Input must be array');
      }

      if (this.isInverse) {
        this.result = this.decode(data);
      } else {
        this.result = this.encode(data);
      }
    }

    /**
   * Get cipher result (encrypted or decrypted data)
   * @returns {uint8[]} Processed output bytes
   * @throws {Error} If key not set, no data fed, or invalid input length
   */

    Result() {
      if (this.result === null) {
        throw new Error('WozencraftEnsembleInstance.Result: Call Feed() first to process data');
      }
      return this.result;
    }

    encode(data) {
      if (data.length !== 2) {
        throw new Error('Wozencraft encode: Input must be exactly 2 bits');
      }

      // Linear encoding: c = m * G
      const codeword = new Array(4).fill(0);

      for (let i = 0; i < 4; ++i) {
        let sum = 0;
        for (let j = 0; j < 2; ++j) {
          sum ^= (data[j] & this.generator[j][i]);
        }
        codeword[i] = sum;
      }

      return codeword;
    }

    decode(data) {
      if (data.length !== 4) {
        throw new Error('Wozencraft decode: Input must be exactly 4 bits');
      }

      // Maximum likelihood decoding (try all 2^2 = 4 messages)
      let minDistance = Infinity;
      let bestMessage = [0, 0];

      for (let m = 0; m < 4; ++m) {
        const message = [(m >> 1) & 1, m & 1];
        const testCodeword = this.encode(message);

        // Calculate Hamming distance
        let distance = 0;
        for (let i = 0; i < 4; ++i) {
          if (data[i] !== testCodeword[i]) {
            ++distance;
          }
        }

        if (distance < minDistance) {
          minDistance = distance;
          bestMessage = message;
        }
      }

      return bestMessage;
    }

    DetectError(data) {
      if (data.length !== 4) return true;

      try {
        const decoded = this.decode(data);
        const reencoded = this.encode(decoded);

        for (let i = 0; i < 4; ++i) {
          if (data[i] !== reencoded[i]) {
            return true;
          }
        }
        return false;
      } catch (e) {
        return true;
      }
    }
  }

