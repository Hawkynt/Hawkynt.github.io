#include "BaseKing.h"
 
extern void BaseKing(const u16 *k, u16 *a, const u16 *RC);

/*==================================================================================*/
/* Round Constants for encryption are computed as follows:
 *  RCE[0] = 0x000B;
 *  for (i=1; i<12; i++) if (RCE[i-1] & 0x0080) RCE[i] = (RCE[i-1]<<1) & 0x0111;
 *                                         else RCE[i] =  RCE[i-1]<<1;
/*----------------------------------------------------------------------------------*/
u16 RCE[12] = {0x0B,0x16,0x2C,0x58,0xB0,0x71,0xE2,0xD5,0xBB,0x67,0xCE,0x8D};
/*==================================================================================*/

/*==================================================================================*/
void BASEKINGencrypt(const struct BASEKINGstruct * const sp, 
                     u16* const a)
/*==================================================================================*/
{
  BaseKing(sp->ke,a,RCE);
}

