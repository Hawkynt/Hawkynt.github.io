#include "BaseKing.h"
 
extern void BaseKing(const u16 *k, u16 *a, const u16 *RC);

/*==================================================================================*/
/* Round Constants for decryption are computed as follows:
 *  RCD[0] = 0x008D; 
 *  for (i=1; i<12; i++) if (RCE[i-1] & 0x0001) RCE[i] = (RCE[i-1] & 0x0111)>>1;
 *                                         else RCE[i] =  RCE[i-1]>>1;
 *----------------------------------------------------------------------------------*/
u16 RCD[12] = {0x8D,0xCE,0x67,0xBB,0xD5,0xE2,0x71,0xB0,0x58,0x2C,0x16,0x0B};
/*==================================================================================*/

/*==================================================================================*/
void BASEKINGdecrypt(const struct BASEKINGstruct * const sp, 
                     u16* const a)
/*==================================================================================*/
{
  BaseKing(sp->kd,a,RCD);
}

